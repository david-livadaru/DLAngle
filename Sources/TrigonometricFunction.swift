import Foundation

public enum TrigonometricFunction: String {
    case sin, cos, tan, cot, sec, csc // trigonometric functions
    case asin, acos, atan, atan2, acot, asec, acsc // inverse trigonometric functions
    case sinh, cosh, tanh, coth, sech, csch // hyperbolic functions
    case asinh, acosh, atanh, acoth, asech, acsch // inverse hyperbolic functions
}
